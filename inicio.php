  <h1>UNAS PALABRAS SOBRE EL PROYECTO</h1>
  
    <p>Paraíso Aborigen (Patrimonio Canario) pretende englobar una vista auténtica, crítica y sin límites sobre los yacimientos y modos de vida de los antiguos canarios.</p>
    <p>Pretendemos abordar tanto su estructura como historia a fin de conocer mucho más, mediante teorías e información constatada un poco más sobre nuestra cultura.</p>
    <p>Este proyecto no busca más que expandir el conocimiento y respeto sobre nuestra antigua cultura, toda aportación es bien recibida.</p>
    <p>No tenemos ningún modo de financiación salvo nuestros propios recursos, las ganas de explorar, descubrir y compartir con todos.</p>
    
